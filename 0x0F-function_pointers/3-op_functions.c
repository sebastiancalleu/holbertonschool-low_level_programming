#include "3-calc.h"
/**
 * op_add - this function sums two number.
 * @a: the first number.
 * @b: the second number.
 * Return: the result.
 */
int op_add(int a, int b)
{
	int c;

	c = a + b;
return (c);
}
/**
 * op_sub - this function substract two numbers.
 * @a: the first number.
 * @b: the second number.
 * Return: the result.
 */
int op_sub(int a, int b)
{
	int c;

	c = a - b;
return (c);
}
/**
 * op_mul - this function multiply two numbers.
 * @a: the first number.
 * @b: the second number.
 * Return: the result.
 */
int op_mul(int a, int b)
{
	int c;

	c = a * b;
return (c);
}
/**
 * op_div - this function divide two numbers.
 * @a: the first number.
 * @b: the second number.
 * Return: the result.
 */
int op_div(int a, int b)
{
	int c;

	c = a / b;
return (c);
}
/**
 * op_mod - this function reach the modulo.
 * @a: the first number.
 * @b: the second number.
 * Return: the result.
 */
int op_mod(int a, int b)
{
	int c;

	c = a % b;
return (c);
}
